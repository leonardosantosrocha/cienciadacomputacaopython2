# Igualdade entre strings
x = 'claro'
y = 'escuro'
print(x == y)

# Diferença entre strings
x = 'pequeno'
y = 'grande'
print(x != y)

# Igualdade em memória
x = 'leonardo'
y = 'Leonardo'
z = 'leonardo'
print(x is y, x is z)

# Comparação entre strings (alfabética, as maiúsculas vem primeiro)
x = 'amor'
y = 'odio'
print(x < y, x > y)