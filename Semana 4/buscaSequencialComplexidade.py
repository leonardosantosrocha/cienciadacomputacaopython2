#   - A complexidade computacional da busca sequencial é
#       - n, no pior caso
#       - n/2, no caso médio

# Conclusão
#   - Busca sequêncial é boa pois é bem simples.
#   - Funciona bem quando a busca é feito num volume pequeno de dados
